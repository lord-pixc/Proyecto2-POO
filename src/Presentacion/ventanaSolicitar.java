    /*
    * Carga los datos en los ComboBoxes de Cliente y Servicio
    */
    private void cargarCombos() {
       // Limpiar combos
       jComboBoxCliente.removeAllItems();
       jComboBoxServicio.removeAllItems();

       // Cargar clientes
       jComboBoxCliente.addItem("-- Seleccione un cliente --");
       for (Cliente cliente : gestor.getClientes()) {
           // Mostrar: "ID - Nombre"
           jComboBoxCliente.addItem(cliente.getId() + " - " + cliente.getNombre());
       }

       // Cargar servicios
       jComboBoxServicio.addItem("-- Seleccione un servicio --");
       for (Servicio servicio : gestor.getServicios()) {
           // Mostrar: "ID - Nombre - Precio"
           jComboBoxServicio.addItem(servicio.getId() + " - " + servicio.getNombre() + " - ₡" + servicio.getPrecio());
       }
    }

    /*
    * Extrae el ID de un item del ComboBox
    * Ejemplo: "300000111 - Juan Perez" → "300000111"
    */
    private String extraerIdDeCombo(String itemCombo) {
       if (itemCombo == null || !itemCombo.contains(" - ")) {
           return null;
       }
       return itemCombo.split(" - ")[0].trim();
    }

