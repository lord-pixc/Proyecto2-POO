    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
    // 1. VALIDAR CAMPOS
    String placa = jTextFieldPlaca.getText().trim();
    
    if (placa.isEmpty() || jComboBoxCliente.getSelectedIndex() == 0 || jComboBoxServicio.getSelectedIndex() == 0) {
        JOptionPane.showMessageDialog(this, "Complete todos los campos obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // 2. EXTRAER DATOS
    String idCliente = extraerIdDeCombo((String) jComboBoxCliente.getSelectedItem());
    String idServicio = extraerIdDeCombo((String) jComboBoxServicio.getSelectedItem());
    String observaciones = jTextAreaObs.getText().trim();
    
    // 3. CREAR Y GUARDAR SOLICITUD
    Solicitud nueva = new Solicitud();
    nueva.setId(gestor.generarIdSolicitud());
    nueva.setPlaca(placa);
    nueva.setIdCliente(idCliente);
    nueva.setIdServicio(idServicio);
    nueva.setIdEstado("000");
    nueva.setObservaciones(observaciones.isEmpty() ? "Sin observaciones" : observaciones);
    
    gestor.getSolicitudes().add(nueva);
    gestor.guardarSolicitudes();
    
    // 4. MOSTRAR MENSAJE Y CERRAR
    JOptionPane.showMessageDialog(this, 
        "Solicitud guardada exitosamente\nConsecutivo: " + nueva.getId(), 
        "Éxito", 
        JOptionPane.INFORMATION_MESSAGE);
    
    dispose();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

